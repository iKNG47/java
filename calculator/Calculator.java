package calculator;

public class Calculator {
     static double add(double x, double y){
        double sum;
        sum=x+y;
        return sum;
    }

    static double subtract(double x, double y){
        double sub;
        sub=x-y;
        return sub;
    }

    static double multiply(double x, double y){
        double mul;
        mul=x*y;
        return mul;
    }

    static double divide(double x, double y){
        double div;
        div=x/y;
        return div;
    }

}
